-- Security definer function to check session membership
CREATE OR REPLACE FUNCTION public.is_session_member(_user_id UUID, _session_id UUID)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.sessions WHERE id = _session_id AND owner_id = _user_id
    UNION
    SELECT 1 FROM public.session_members WHERE session_id = _session_id AND user_id = _user_id
  )
$$;

-- Security definer function to check session role
CREATE OR REPLACE FUNCTION public.get_session_role(_user_id UUID, _session_id UUID)
RETURNS app_role
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT CASE
    WHEN EXISTS (SELECT 1 FROM public.sessions WHERE id = _session_id AND owner_id = _user_id) THEN 'owner'::app_role
    ELSE (SELECT role FROM public.session_members WHERE session_id = _session_id AND user_id = _user_id)
  END
$$;

-- Security definer function to check if user has specific role
CREATE OR REPLACE FUNCTION public.has_role(_user_id UUID, _role app_role)
RETURNS BOOLEAN
LANGUAGE sql
STABLE
SECURITY DEFINER
SET search_path = public
AS $$
  SELECT EXISTS (
    SELECT 1 FROM public.user_roles WHERE user_id = _user_id AND role = _role
  )
$$;

