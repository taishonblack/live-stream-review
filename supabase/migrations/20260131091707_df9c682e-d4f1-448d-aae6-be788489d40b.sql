  input_id UUID REFERENCES public.session_inputs(id) ON DELETE CASCADE NOT NULL UNIQUE,
  video_codec TEXT,
  video_resolution TEXT,
  video_fps DECIMAL(5,2),
  video_bitrate_kbps INTEGER,
  network_rtt_ms INTEGER,
  network_packet_loss DECIMAL(5,2),
  network_retransmits INTEGER,
  network_jitter_ms INTEGER,
  audio_lufs_m DECIMAL(5,1),
  audio_lufs_s DECIMAL(5,1),
  audio_lufs_i DECIMAL(5,1),
  audio_peak_db DECIMAL(5,1),
  audio_true_peak_db DECIMAL(5,1),
  audio_channels TEXT,
